int SwordOfferSolution::findRepeatNumber(vector<int>& nums)
{
    //注意：在一个长度为 n 的数组 nums 里的所有数字都在 0～n-1 的范围内
    int length = nums.size();
    int i = 0;

    while (i < length) {
        if (nums[i] == i) {
            i++;
            continue;
        }

        if (nums[i] == nums[nums[i]])
            return nums[i];

        int temp = nums[i];
        nums[i] = nums[temp];
        nums[temp] = temp;
    }

    return -1;
}
